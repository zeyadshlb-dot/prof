-- 6. BUSINESS LOGIC (الدالة السحرية)
-- =============================================

-- دالة تفعيل الكود (يتم استدعاؤها من التطبيق)
create or replace function redeem_code(code_input text)
returns jsonb language plpgsql security definer as $$
declare
  code_record record;
  user_id uuid := auth.uid();
begin
  -- 1. التحقق من وجود الكود
  select * into code_record from activation_codes 
  where code = code_input;

  if not found then
    return jsonb_build_object('success', false, 'message', 'الكود غير صحيح، تأكد من الكتابة');
  end if;

  if code_record.is_used then
    return jsonb_build_object('success', false, 'message', 'عفواً، هذا الكود تم استخدامه مسبقاً');
  end if;

  -- 2. التحقق من الاشتراك المسبق (لمنع تضييع الكود)
  if code_record.type = 'course' then
    if exists (select 1 from enrollments where student_id = user_id and course_id = code_record.target_id) then
      return jsonb_build_object('success', false, 'message', 'أنت مشترك بالفعل في هذا الكورس');
    end if;
    
    -- إضافة الاشتراك
    insert into enrollments (student_id, course_id) values (user_id, code_record.target_id);

  elsif code_record.type = 'bundle' then
    if exists (select 1 from enrollments where student_id = user_id and bundle_id = code_record.target_id) then
      return jsonb_build_object('success', false, 'message', 'أنت مشترك بالفعل في هذه الباقة');
    end if;

    -- إضافة الاشتراك
    insert into enrollments (student_id, bundle_id) values (user_id, code_record.target_id);
  end if;

  -- 3. حرق الكود
  update activation_codes 
  set is_used = true, used_by_student_id = user_id, used_at = now()
  where code = code_input;

  return jsonb_build_object('success', true, 'message', 'تم تفعيل الاشتراك بنجاح!');
end;
$$;

-- =============================================
-- 7. ROW LEVEL SECURITY (RLS) - الأمان
-- =============================================

-- تفعيل الـ RLS لكل الجداول
alter table profiles enable row level security;
alter table enrollments enable row level security;
alter table student_progress enable row level security;
alter table exam_results enable row level security;

-- قواعد الأمان (Policies)

-- 1. البروفايل: الطالب يشوف ويعدل بياناته فقط
create policy "Users can view own profile" on profiles for select using (auth.uid() = id);
create policy "Users can update own profile" on profiles for update using (auth.uid() = id);

-- 2. الاشتراكات: الطالب يشوف اشتراكات فقط
create policy "Users can view own enrollments" on enrollments for select using (auth.uid() = student_id);

-- 3. التقدم والنتائج: خاص بالطالب فقط
create policy "Users view own progress" on student_progress for all using (auth.uid() = student_id);
create policy "Users view own exam results" on exam_results for select using (auth.uid() = student_id);
create policy "Users insert exam results" on exam_results for insert with check (auth.uid() = student_id);

-- 4. المحتوى العام (الكورسات والباقات): متاح للجميع (للعرض في المتجر)
alter table courses enable row level security;
create policy "Public courses view" on courses for select using (true);
alter table bundles enable row level security;
create policy "Public bundles view" on bundles for select using (true);
alter table lessons enable row level security;
create policy "Public lessons view" on lessons for select using (true); -- (ممكن تقيدها لو حابب، بس الأسهل تخلي العناوين عامة والفيديو محمي في الفرونت)

-- =============================================
-- 8. TRIGGERS (أتمتة التواريخ)
-- =============================================
create trigger update_profiles_modtime before update on profiles 
for each row execute procedure update_updated_at_column();

create trigger update_progress_modtime before update on student_progress 
for each row execute procedure update_updated_at_column();


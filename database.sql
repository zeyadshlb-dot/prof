-- =============================================
-- 1. EXTENSIONS & UTILS
-- =============================================
-- تفعيل الامتدادات الضرورية
create extension if not exists "uuid-ossp";

-- دالة لتحديث التوقيت تلقائياً
create or replace function update_updated_at_column()
returns trigger as $$
begin
    new.updated_at = now();
    return new;
end;
$$ language 'plpgsql';

-- =============================================
-- 2. USERS & SECURITY (المستخدمين وحماية الأجهزة)
-- =============================================

-- جدول بيانات المستخدمين الإضافية
create table public.profiles (
  id uuid references auth.users(id) on delete cascade primary key,
  full_name text not null,
  phone_number text,
  academic_year text, -- السنة الدراسية (3 ثانوي، إلخ)
  role text check (role in ('admin', 'student', 'teacher')) default 'student',
  is_banned boolean default false,
  created_at timestamp with time zone default now(),
  updated_at timestamp with time zone default now()
);

-- جدول بصمة الأجهزة (لمعرفة عدد الأجهزة لكل طالب)
create table public.student_devices (
  id bigint generated by default as identity primary key,
  student_id uuid references public.profiles(id) on delete cascade,
  device_id text not null, -- Unique ID from Expo Application
  device_name text, -- e.g. "Samsung S21"
  last_active timestamp default now(),
  created_at timestamp default now(),
  unique(student_id, device_id) -- منع تكرار نفس الجهاز لنفس الطالب
);

-- =============================================
-- 3. CONTENT STRUCTURE (هيكل المحتوى)
-- =============================================

-- التصنيفات (مثال: الصف الأول، الصف الثاني)
create table public.categories (
  id bigint generated by default as identity primary key,
  name text not null,
  sort_order int default 0,
  created_at timestamp default now()
);

-- الكورسات
create table public.courses (
  id uuid default gen_random_uuid() primary key,
  category_id bigint references public.categories(id),
  title text not null,
  description text,
  thumbnail_url text,
  price decimal default 0, -- للعرض فقط
  is_published boolean default false,
  sort_order int default 0,
  created_at timestamp default now()
);

-- الباقات (تجميعة كورسات)
create table public.bundles (
  id uuid default gen_random_uuid() primary key,
  title text not null,
  description text,
  thumbnail_url text,
  price decimal default 0, -- للعرض فقط
  is_published boolean default false,
  created_at timestamp default now()
);

-- ربط الكورسات بالباقات (Many-to-Many)
create table public.bundle_items (
  bundle_id uuid references public.bundles(id) on delete cascade,
  course_id uuid references public.courses(id) on delete cascade,
  primary key (bundle_id, course_id)
);

-- الفصول داخل الكورس
create table public.chapters (
  id uuid default gen_random_uuid() primary key,
  course_id uuid references public.courses(id) on delete cascade,
  title text not null,
  sort_order int default 0,
  created_at timestamp default now()
);

-- الدروس (فيديو، امتحان، ملف)
create table public.lessons (
  id uuid default gen_random_uuid() primary key,
  chapter_id uuid references public.chapters(id) on delete cascade,
  title text not null,
  type text check (type in ('video', 'exam', 'pdf')) default 'video',
  
  -- Video Details
  video_url text, -- Youtube ID or Bunny ID
  video_provider text default 'bunny', -- youtube, bunny, vimeo
  duration_minutes int default 0,
  
  -- Exam Details (لو الدرس امتحان)
  exam_questions jsonb, -- [{question: "...", options: [], answer: 1}]
  passing_score int default 50,
  allowed_attempts int default 0, -- 0 means unlimited
  
  -- Settings
  is_free_preview boolean default false, -- درس مجاني
  sort_order int default 0,
  created_at timestamp default now()
);

-- =============================================
-- 4. ACCESS & ACTIVATION (الاشتراكات والأكواد)
-- =============================================

-- جدول الاشتراكات النشطة
create table public.enrollments (
  id uuid default gen_random_uuid() primary key,
  student_id uuid references public.profiles(id) on delete cascade,
  
  -- الطالب ممكن يشترك في كورس مفرد أو باقة
  course_id uuid references public.courses(id) on delete cascade,
  bundle_id uuid references public.bundles(id) on delete cascade,
  
  activated_at timestamp default now(),
  expires_at timestamp, -- اختياري (لو الاشتراك بمدة)
  
  -- قيد: لازم حقل واحد منهم يكون مليان
  check (
    (course_id is not null and bundle_id is null) or 
    (course_id is null and bundle_id is not null)
  )
);

-- جدول أكواد التفعيل (الكروت)
create table public.activation_codes (
  code text primary key, -- الكود المطبوع (e.g. ZEYAD-2026-XYZ)
  type text check (type in ('course', 'bundle')) not null,
  target_id uuid not null, -- ID الكورس أو الباقة
  
  is_used boolean default false,
  used_by_student_id uuid references public.profiles(id),
  used_at timestamp,
  created_at timestamp default now()
);

-- =============================================
-- 5. PROGRESS & RESULTS (متابعة الطالب)
-- =============================================

-- متابعة مشاهدة الدروس
create table public.student_progress (
  student_id uuid references public.profiles(id) on delete cascade,
  lesson_id uuid references public.lessons(id) on delete cascade,
  is_completed boolean default false,
  last_position_seconds int default 0, -- وقف عند الدقيقة كام
  updated_at timestamp default now(),
  primary key (student_id, lesson_id)
);

-- نتائج الامتحانات
create table public.exam_results (
  id uuid default gen_random_uuid() primary key,
  student_id uuid references public.profiles(id) on delete cascade,
  lesson_id uuid references public.lessons(id) on delete cascade, -- الامتحان مرتبط بدرس
  score int not null,
  total_score int not null,
  attempt_number int default 1,
  is_passed boolean not null,
  details jsonb, -- تفاصيل الإجابات
  created_at timestamp default now()
);

